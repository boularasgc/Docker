#include "libft.h"

void	ft_strupper(char s[])
{
	int c;

	c = 0;
	while (s[c] != '\0')
	{
		if (s[c] >= 'a' && s[c] <= 'z')
		{
			s[c] = s[c] - 32;
		}
		c++;
	}
}
